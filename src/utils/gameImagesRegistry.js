// Helper function to get a character image by name or filename
export const getCharacterImage = (identifier) => {
  return characterImages[identifier] || null
}

// Helper function to get a background image by name or ID
export const getBackgroundImage = (identifier) => {
  // First try to get by direct name
  if (backgroundImages[identifier]) {
    return backgroundImages[identifier]
  }

  // For backward compatibility, check if it's a numeric ID
  if (typeof identifier === 'number' || !isNaN(parseInt(identifier))) {
    return backgroundImagesById[parseInt(identifier)] || null
  }

  return null
}

